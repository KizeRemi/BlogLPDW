<?php

namespace BlogBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * Article
 *
 * @ORM\Table(name="commentaires")
 * @ORM\Entity(repositoryClass="BlogBundle\Repository\CommentRepository")
 */

class Comment
{
	/**
    * @var int
    *
    * @ORM\Column(name="id", type="integer")
    * @ORM\Id
    * @ORM\GeneratedValue(strategy="AUTO")
    */
    private $id;

    /**
     * @var string
     *
     * @Assert\NotBlank()
     * @Assert\Length(
     *      min = 20,
     *      max = 255,
     *      minMessage = "Le message est trop court ",
     *      maxMessage = "Le message ne doit pas dépasser 255 caractères."
     * )
     * @ORM\Column(name="content", type="text")
    */
    private $content;

    /**
     * @var string
     *
     * @Assert\NotBlank()
     * @Assert\Regex(
     *     pattern     = "/^[a-z]+$/i",
     *     htmlPattern = "^[a-zA-Z]+$",
     *     message= "Ce champ ne peut comporter que des lettres"
     * )
     * @Assert\Length(
     *      min = 5,
     *      max = 10,
     *      minMessage = "Le pseudo est trop court ",
     *      maxMessage = "Le pseudo est trop long"
     * )
     * @ORM\Column(name="author", type="text")
     */
    private $author;
    /**
     * @var \DateTime
     *
     * @Assert\Date()
     * @ORM\Column(name="date", type="datetime")
     */
    private $date;
    /**
    * @ORM\ManyToOne(targetEntity="Article")
    * @ORM\JoinColumn(name="article_id", referencedColumnName="id")
    */
    private $article;

    public function __construct()
    {
        $this->date = new \DateTime();
    }
 

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return Comment
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set author
     *
     * @param string $author
     *
     * @return Comment
     */
    public function setAuthor($author)
    {
        $this->author = $author;

        return $this;
    }

    /**
     * Get author
     *
     * @return string
     */
    public function getAuthor()
    {
        return $this->author;
    }

    /**
     * Set date
     *
     * @param \DateTime $date
     *
     * @return Comment
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set category
     *
     * @param \BlogBundle\Entity\Article $category
     *
     * @return Comment
     */
    public function setArticle(\BlogBundle\Entity\Article $article = null)
    {
        $this->article = $article;

        return $this;
    }

    /**
     * Get article
     *
     * @return \BlogBundle\Entity\Article
     */
    public function getArticle()
    {
        return $this->article;
    }
}
